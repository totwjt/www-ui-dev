// 模糊查找函数
export const searchCountries = (keyword = '') => {
  if (!keyword.trim()) {
    return countriesGrouped // 如果关键字为空，返回完整列表
  }

  const keywordLower = keyword.toLowerCase()
  const filteredGroups = countriesGrouped
    .map(group => {
      const filteredCountries = group.countries.filter(country =>
        country.name.toLowerCase().includes(keywordLower) ||
        country.code.toLowerCase().includes(keywordLower)
      )
      return {
        letter: group.letter,
        countries: filteredCountries
      }
    })
    .filter(group => group.countries.length > 0) // 过滤掉空分组

  return filteredGroups
}
